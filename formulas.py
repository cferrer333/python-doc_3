def sq_ft():
    length = input("What is the length in feet? ")
    width = input("What is the width in feet? ")
    squared = int(length) * int(width)
    print(f"The square footage of the house is {squared} sq ft")


def circle_circum():
    diameter = input("What is the diameter in inches?")
    circumference = int(diameter) * 3.14
    print(f"The circumference of this circle is {circumference} inches")
